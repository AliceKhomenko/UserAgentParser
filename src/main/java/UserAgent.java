public class UserAgent {
    String browserName;
    String browserVersion;
    String operationSystem;
    String hardwareVendor;
    String hardwareModel;
    int screenWidth;
    int screenHeight;
    boolean isItDesktopDevice;
    boolean isItMobileDevice;
    boolean isItTablet;
    boolean isItCrawlerOrRobot;
    boolean isItConsole;

    public UserAgent() {
        this.browserName = browserName;
        this.browserVersion = browserVersion;
        this.operationSystem = operationSystem;
        this.hardwareVendor = hardwareVendor;
        this.hardwareModel = hardwareModel;
        this.screenWidth = screenWidth;
        this.screenHeight = screenHeight;
        this.isItDesktopDevice = isItDesktopDevice;
        this.isItMobileDevice = isItMobileDevice;
        this.isItTablet = isItTablet;
        this.isItCrawlerOrRobot = isItCrawlerOrRobot;
        this.isItConsole = isItConsole;


    }

}
